import java.util.Scanner;
class StrongNumber {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int n = sc.nextInt();
        int sum = 0;
        int temp = n;

        while (temp > 0) {
            int digit = temp % 10;
            int fact = 1;
            for (int i = 1; i <= digit; i++) {
                fact *= i;
            }
            sum += fact;
            temp /= 10;
        }

        if (sum == n) {
            System.out.println(n + " is a Strong Number");
        } else {
            System.out.println(n + " is not a Strong Number");
        }
    }
}
